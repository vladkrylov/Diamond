function numbers_scan_plot_all
    N_bins = 1000;
    
    files = {
        'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e1.csv'...
        ,'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e2.csv'...
        ,'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e3.csv'...
        ,'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e4.csv'...
        ,'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e5.csv'...
        ,'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e6.csv'...
        ,'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e7.csv'...
        ,'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e8.csv'...
        ,'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e9.csv'...
        ,'numbers_scan_E=Gauss(2.5MeV, 0.2MeV)/e10.csv'...
        ,'numbers_scan_2.5MeV/e1.csv'...
        ,'numbers_scan_2.5MeV/e2.csv'...
        ,'numbers_scan_2.5MeV/e3.csv'...
        ,'numbers_scan_2.5MeV/e4.csv'...
        ,'numbers_scan_2.5MeV/e5.csv'...
        ,'numbers_scan_2.5MeV/e6.csv'...
        ,'numbers_scan_2.5MeV/e7.csv'...
        ,'numbers_scan_2.5MeV/e8.csv'...
        ,'numbers_scan_2.5MeV/e9.csv'...
        ,'numbers_scan_2.5MeV/e10.csv'...
        };
    
    L = length(files);
    x = zeros(N_bins, L);
    y = zeros(N_bins, L);
    legendInfo = cell(1,L);
    
    for i=1:L
        data = load(files{i});
        [counts, centers] = hist(data, N_bins);
        x(:,i) = centers.';
        y(:,i) = smooth(counts.', 1);
        
        [pathstr,name,ext] = fileparts(files{i});
        legendInfo{i} = name;
    end
    
    figure(1)
    hold on
        stairs(x(:,1:10), y(:,1:10))
        stairs(x(:,11:20), y(:,11:20), 'o')
    hold off
    legend(legendInfo)
    title('E_{primaries 1} = 2.5MeV;  E_{primaries 2} = Gauss(mean = 2.5MeV, sigma = 0.2MeV)')
    xlabel('E_{deposit}, MeV')
    ylabel('Counts')
end